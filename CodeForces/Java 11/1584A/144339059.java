

import java.util.Scanner;

public class solution{
	
 public static void main(String[] args){

 Scanner sc=new Scanner(System.in);
 long t=sc.nextLong();
 long u,v,x,y;
 while(t>0){
 u=sc.nextLong();
 v=sc.nextLong();
 x=-u*u;
 y=v*v;
 System.out.println(x+" "+y);
 t--;

 }
 }

}